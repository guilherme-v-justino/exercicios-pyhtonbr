"""Faça um Programa que calcule a área de um quadrado, em seguida mostre o
dobro desta área para o usuário. """

lado = float(input("Digite o tamanho do lado do quadrado: "))
area = lado ** 2
print(f'A área deste quadrado é igual a: {area}')
print(f'O dobro da área calculada é igual a: {area * 2}')
